import java.util.Scanner;

public class Cobol3D {
    private static final String[] cobolText = {
        "________/\\\\\\\\\\____",
        "____/\\\\\\________/\\\\\\\\\\\\\\__________/\\\\\\________",
        "/\\\_____________         _____/\\\////////_______/\\\//",
        "/\\\_____\/\\\/////////\\\______/\\\///\\\_____\/\\\____",
        "_________         ___/\\\/______________/\\\/__\\///\\\__",
        "_\\/\\\_______\\/\\\____/\\\/__\\///\\\___\\/\\\____________",
        "_         __/\\\_______________/\\\______\\//\\\__\\/\\\\\\",
        "\\\\\\\\\____/\\\______\\//\\\__\\/\\\_____________",
        "  _\\/\\\______________\\/\\\_______\\/\\\__\\/\\\/////////\\\\",
        "__\\/\\\_______\\/\\\__\\/\\\_____________         _\\//\\\_",
        "____________\\//\\\______/\\\___\\/\\\_______\\/\\\__\\//\\\\",
        "______/\\\___\\/\\\_____________         __\\///\\\_______",
        "_____\///\\\__/\\\_____\\/\\\_______\\/\\\___\\///\\\__/\\\\",
        "_____\\/\\\_____________         ____\\////\\\\\\\\\\_____\\",
        "///\\\\\/______\\/\\\\\\\\\\\\\\/______\\///\\\\\/______\\/\\",
        "\\\\\\\\\\\\\\_         _______\\/////////________\\/////\\",
        "_______\\/////////////__________\\/////________\\//////////",
        "/////__"
    };

    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            int j = 0;
            while (j < 103) {
                if (cobolText[i].substring(j, j + 4).equals("\\\\/")) {
                    System.out.print(cobolText[i].substring(j, j + 3));
                    System.out.print("\u001B[30m" + cobolText[i].substring(j + 3, j + 4) + "\u001B[0m");
                    j += 4;
                    continue;
                }

                if (cobolText[i].charAt(j) == '/' ||
                    (j + 1 < 103 && cobolText[i].charAt(j + 1) == '/') ||
                    (j + 2 < 103 && cobolText[i].substring(j + 1, j + 3).equals("\\/"))) {
                    System.out.print("\u001B[30m" + cobolText[i].charAt(j) + "\u001B[0m");
                } else {
                    System.out.print("\u001B[37m" + cobolText[i].charAt(j) + "\u001B[0m");
                }
                j++;
            }
            System.out.println();
        }

        System.out.print("Press enter to stop appreciating COBOL in 3D.");
        Scanner scanner = new Scanner(System.in);
        scanner.nextLine();
    }
}